#include "libft.h"

char	*ft_strndup(const char *str, size_t n)
{
	size_t	len;
	char	*dup;
	size_t	i;

	if (str == NULL)
		return (NULL);
	len = ft_strlen(str);
	if (n < len)
		len = n;

	dup = (char *)malloc(len + 1);
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dup[i] = str[i];
		i++;
	}
	dup[len] = '\0';
	return (dup);
}
